package Program;

public class Swapping {
    public static void main(String[] args){
    	
        int a = 10, b = 20;
        int temp;
        System.out.println("a and b brfore swapping: " + a + " " + b);
        temp = a;
        a = b;
        b = temp;
        System.out.println("a and b after swapping: " + a + " " + b);
        
    }
    

}
